import UIKit

extension UIColor {
    static var lightRed: UIColor {
        return UIColor(red: 0.9, green: 0, blue: 0, alpha: 0.5)
    }

    static var lightBlue: UIColor {
        return UIColor(red: 0, green: 0, blue: 0.9, alpha: 0.5)
    }

    static var lightGreen: UIColor {
        return UIColor(red: 0, green: 0.9, blue: 0, alpha: 0.5)
    }

    static var lightWhite: UIColor {
        return UIColor(red: 0.9, green: 0.9, blue: 0.9, alpha: 0.5)
    }
}
